// Use this to programmatically update text in your HTML
window.updateContent = function (selector, key) {
  const element = document.querySelector(selector);
  if (element) {
    const keys = key.split(".");
    let value = window.AC_CONTENT;
    for (let k of keys) {
      value = value[k];
    }
    if (typeof value === "string") {
      element.textContent = value;
    }
  }
};
